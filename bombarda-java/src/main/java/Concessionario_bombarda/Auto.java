public class Auto {

    private String marca;
    private String colore;

    public Auto(String marca, String colore) {
        this.marca = marca;
        this.colore = colore;
    }


    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getColore() {
        return colore;
    }

    public void setColore(String colore) {
        this.colore = colore;
    }
    
    

}
